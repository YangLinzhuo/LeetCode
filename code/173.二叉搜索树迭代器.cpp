#include <stack>
using namespace std;

struct TreeNode {
     int val;
     TreeNode *left;
     TreeNode *right;
     TreeNode() : val(0), left(nullptr), right(nullptr) {}
     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
};

// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class BSTIterator {
public:
    stack<TreeNode*> s;
    BSTIterator(TreeNode* root) {
        dfsLeft(root);
    }
    
    int next() {
        TreeNode* ptr = s.top();
        s.pop();
        int val = ptr->val;
        ptr = ptr->right;
        dfsLeft(ptr);
        return val;
    }
    
    bool hasNext() {
        return !s.empty();
    }

    void dfsLeft(TreeNode* root) {
        while (root != nullptr) {
            s.push(root);
            root = root->left;
        }
    }
};

